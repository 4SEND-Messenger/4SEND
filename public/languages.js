window.currentLang = (function() {
    var saved = localStorage.getItem('4send_language');
    if (saved) return saved;
    return 'en';
})();

window.t = function(key, params) {
    if (!key) return '';
    var lang = window.currentLang || 'ru';
    var str = (window.LANG[lang] && window.LANG[lang][key]) 
            || (window.LANG.ru && window.LANG.ru[key]) 
            || key;
    if (params) {
        Object.keys(params).forEach(function(k) {
            str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
        });
    }
    return str;
};

window.t_plural = function(key, count) {
    var lang = window.currentLang || 'ru';
    var forms = window.LANG[lang] && window.LANG[lang][key];
    if (!forms || !Array.isArray(forms)) return key;
    if (lang === 'en') return count === 1 ? forms[0] : (forms[1] || forms[0]);
    var mod10 = count % 10;
    var mod100 = count % 100;
    if (mod100 >= 11 && mod100 <= 19) return forms[2];
    if (mod10 === 1) return forms[0];
    if (mod10 >= 2 && mod10 <= 4) return forms[1];
    return forms[2];
};

window.applyTranslations = function() {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        el.textContent = window.t(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
        el.placeholder = window.t(el.getAttribute('data-i18n-placeholder'));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
        el.title = window.t(el.getAttribute('data-i18n-title'));
    });
};

document.addEventListener('DOMContentLoaded', function() {
    if (window.currentLang && window.currentLang !== 'ru') {
        applyTranslations();
    }
});
